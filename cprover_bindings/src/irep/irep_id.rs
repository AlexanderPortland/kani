#[allow(clippy::to_string_trait_impl)]
impl ToString for IrepId {
    fn to_string(&self) -> String {
        match self.to_string_maybe_ref() {
            Ok(s) => s,
            Err(r) => r.to_owned(),
        }
    }
}

impl IrepId {
    pub fn to_string_maybe_ref(&self) -> Result<String, &'static str> {
        self.to_owned_string().map_err(|_| self.to_static_string())
    }
    fn to_owned_string(&self) -> Result<String, ()> {
        match &self {
            IrepId::FreeformString(s) => Ok(s.to_string()),
            IrepId::FreeformInteger(i) => Ok(i.to_string()),
            IrepId::FreeformBitPattern(i) => Ok(format!("{i:X}")),
            _ => Err(()),
        }
    }
    fn to_static_string(&self) -> &'static str {
        // SAFETY: Since `IrepId` is marked with the `#[repr(u16)]` attribute
        // its layout is a union between `repr(C)` structs (as described in the docs here https://doc.rust-lang.org/std/mem/fn.discriminant.html).
        // Each of these will have a u16 discriminant as it's first field, so we can safely read directly from this pointer.
        let desc = unsafe { *<*const _>::from(self).cast::<u16>() };
        IREPID_NAMES[desc as usize].unwrap()
    }
}

